package type;

public class PrintData {

	public static void main(String[] args) {
		//숫자
		System.out.println(100);
		System.out.println(3.3);
		System.out.println(100 + 10);  //숫자를 더함
		System.out.println("=============");
		
		//문자
		System.out.println('A');  //문자 하나를 쓸때는 홑따옴표
		System.out.println("B");  //문자열 표기 시에는 쌍따옴표
		System.out.println("banana");  //문자열 표기 시에는 쌍따옴표
		System.out.println(100 + "10");  //문자가 연결됨
		System.out.println("=============");
		
		//불리언(boolean)
		System.out.println(true);
		System.out.println(4 > 5);
	}

}
